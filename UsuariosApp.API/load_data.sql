-- Consulta dos perfis e permissões
SELECT 
    P.Nome       AS Perfil,
	PE.Nome      AS Permissao,
    PE.Servico   AS Endpoint,
    PE.Tipo      AS MetodoHttp
FROM PERFIL_PERMISSAO PP
JOIN PERFIL P
    ON PP.PERFIL_ID = P.Id
JOIN PERMISSAO PE
    ON PP.PERMISSAO_ID = PE.Id
ORDER BY P.Nome, PE.Servico, PE.Tipo;
